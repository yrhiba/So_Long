int	ft_miniatoi(const char *str)
{
	int	nbr;
	int	i;

	nbr = 0;
	i = 0;
	while (str[i] <= '9' && str[i] >= '0')
		nbr = (nbr * 10) + (str[i++] - '0');
	return (nbr);
}
