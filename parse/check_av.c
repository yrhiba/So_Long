#include "so_long.h"

int	check_av(int ac, char **av)
{
	size_t	i;

	if (ac != 2)
		return (-1);
	i = ft_strlen(av[1]);
	if (i < 5)
		return (-1);
	if (av[1][i - 1] != 'r' || av[1][i - 2] != 'e' || av[1][i - 3] != 'b'
		|| av[1][i - 4] != '.')
		return (-1);
	return (0);
}
